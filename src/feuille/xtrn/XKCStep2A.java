    public String getCommands(){
        String commands = "";
        for(int i=0;i<tableModel.getRowCount();i++){
            if(i+1<tableModel.getRowCount()){
                commands += (String)tableModel.getValueAt(i, 1) + "ยง";
            }else{
                commands += (String)tableModel.getValueAt(i, 1);
            }
        }
        if(commands.isEmpty()){
            return commands;
        }else{
            return "ยง"+commands;
        }        
    }

