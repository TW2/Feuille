package org.wingate.feuille.subs.ass;

/**
 *
 * @author util2
 */
public class AssAttachment {
    public enum Type {
        None, Font, Image;
    }
    
    private String path;
    private Type type;

    public AssAttachment(String path, Type type) {
        this.path = path;
        this.type = type;
    }

    public AssAttachment() {
        path = null;
        type = Type.None;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public Type getType() {
        return type;
    }

    public void setType(Type type) {
        this.type = type;
    }
}
