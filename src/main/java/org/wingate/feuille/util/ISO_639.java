    public static ISO_639 get(String search){
        ISO_639 iso = English;
        
        for(ISO_639 i : values()){
            if(i.getName().equalsIgnoreCase(search)
                    || i.getSet1().equalsIgnoreCase(search)
                    || i.getSet2T().equalsIgnoreCase(search)
                    || i.getSet2B().equalsIgnoreCase(search)){
                iso = i;
                break;
            }
        }
        
        return iso;
    }

